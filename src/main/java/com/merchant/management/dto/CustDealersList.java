package com.merchant.management.dto;

public class CustDealersList {
	
	private String dealerName;
	private String dealerEmail;
	private String dealerPhNo;
	private String dealerRefId;
	
	
	
	public String getDealerRefId() {
		return dealerRefId;
	}
	public void setDealerRefId(String dealerRefId) {
		this.dealerRefId = dealerRefId;
	}
	public String getDealerName() {
		return dealerName;
	}
	public void setDealerName(String dealerName) {
		this.dealerName = dealerName;
	}
	public String getDealerEmail() {
		return dealerEmail;
	}
	public void setDealerEmail(String dealerEmail) {
		this.dealerEmail = dealerEmail;
	}
	public String getDealerPhNo() {
		return dealerPhNo;
	}
	public void setDealerPhNo(String dealerPhNo) {
		this.dealerPhNo = dealerPhNo;
	}
	
	

}
